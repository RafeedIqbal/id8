async def _load_code_snapshot(ctx: RunContext) -> dict[str, Any] | None:
    result = await ctx.db.execute(
        select(ProjectArtifact)
        .where(
            ProjectArtifact.run_id == ctx.run_id,
            ProjectArtifact.artifact_type == ArtifactType.CODE_SNAPSHOT,
        )
        .order_by(ProjectArtifact.version.desc())
        .limit(1)
    )
    artifact = result.scalar_one_or_none()
    return artifact.content if artifact is not None else None


async def _latest_code_snapshot_artifact(ctx: RunContext) -> ProjectArtifact | None:
    result = await ctx.db.execute(
        select(ProjectArtifact)
        .where(
            ProjectArtifact.run_id == ctx.run_id,
            ProjectArtifact.artifact_type == ArtifactType.CODE_SNAPSHOT,
        )
        .order_by(ProjectArtifact.version.desc())
        .limit(1)
    )
    return result.scalar_one_or_none()


async def _persist_prepare_pr_metadata(ctx: RunContext, metadata: dict[str, Any]) -> None:
    """Persist GitHub/PR metadata onto the latest code_snapshot artifact."""
    artifact = await _latest_code_snapshot_artifact(ctx)
    if artifact is None:
        return

    content = dict(artifact.content or {})
    github_meta = dict(content.get("__github", {}))
    github_meta.update(metadata)
    content["__github"] = github_meta
    artifact.content = content
    await ctx.db.flush()


async def _load_project(ctx: RunContext) -> Project | None:
    result = await ctx.db.execute(select(Project).where(Project.id == ctx.project_id))
    return result.scalar_one_or_none()


async def _create_project_repo(
    ctx: RunContext,
    client: GitHubClient,
    project: Project,
) -> tuple[str, str]:
    """Create a new GitHub repo and persist its URL on the project record."""
    repo_name = _generate_repo_name(ctx.project_id)

    user_data = await client.get_authenticated_user()
    owner: str = user_data["login"]

    repo_info = await client.create_repo(repo_name, private=True)

    # Persist repo URL so future runs/resumes find it.
    project.github_repo_url = repo_info.html_url
    await ctx.db.flush()

    return owner, repo_info.name


def _generate_repo_name(project_id: uuid.UUID) -> str:
    """Derive a stable, slug-safe repo name from the project UUID."""
    short = str(project_id).replace("-", "")[:12]
    return f"id8-{short}"


async def _ensure_branch(
    client: GitHubClient,
    owner: str,
    repo: str,
    branch_name: str,
) -> str:
    """Create the feature branch if it doesn't exist.

    Returns the default branch name (needed for PR base).
    """
    repo_info = await client.get_repo(owner, repo)
    default_branch = repo_info.default_branch

    try:
        await client.get_branch(owner, repo, branch_name)
        logger.info("Branch '%s' already exists on %s/%s — reusing", branch_name, owner, repo)
    except GitHubNotFoundError:
        try:
            await client.create_branch(owner, repo, default_branch, branch_name)
        except GitHubConflictError:
            # Race condition: another process created it just now.
            logger.info("Branch '%s' created concurrently on %s/%s — ok", branch_name, owner, repo)

    return default_branch


async def _ensure_pull_request(
    client: GitHubClient,
    owner: str,
    repo: str,
    branch_name: str,
    *,
    base: str,
    ctx: RunContext,
) -> Any:
    """Return an existing PR for this branch, or create a new one."""
    # Check for an existing open PR from this branch.
    existing = await client.list_pull_requests(
        owner, repo, head=f"{owner}:{branch_name}", state="open"
    )
    if existing:
        logger.info(
            "Found existing PR #%d for branch '%s' on %s/%s — reusing",
            existing[0].number,
            branch_name,
            owner,
            repo,
        )
        return existing[0]

    title = _build_pr_title(ctx)
    body = _PR_BODY_TEMPLATE.format(
        run_id=ctx.run_id,
        project_id=ctx.project_id,
    )
    return await client.create_pull_request(
        owner,
        repo,
        head=branch_name,
        base=base,
        title=title,
        body=body,
    )


async def _find_closed_pull_request(
    client: GitHubClient,
    owner: str,
    repo: str,
    branch_name: str,
) -> PrInfo | None:
    """Return a closed PR for this run branch if one exists and no open PR exists."""
    prs = await client.list_pull_requests(
        owner,
        repo,
        head=f"{owner}:{branch_name}",
        state="all",
    )
    if not prs:
        return None

    if any(pr.state == "open" for pr in prs):
        return None

    for pr in prs:
        if pr.merged:
            return pr
    return prs[0]


def _build_pr_title(ctx: RunContext) -> str:
    """Build a human-readable PR title from the workflow context."""
    prompt: str = ctx.workflow_payload.get("initial_prompt", "")
    if prompt:
        # Truncate to a reasonable headline length.
        truncated = prompt[:72].strip()
        if len(prompt) > 72:
            truncated += "…"
        return f"feat(id8): {truncated}"
    return f"feat(id8): generated code for run {ctx.run_id}"
